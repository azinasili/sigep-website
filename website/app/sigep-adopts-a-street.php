                <h2>SigEp Adopts a Street</h2>
                <span class="text-small">8/19/2013</span>
                <span class="text-small">written by:</span>
                <span class="text-small">Chad Schmidt</span>
              </header>
              <p>
                Adding to the long list of SigEp’s community service
                initiatives, our chapter is now pioneering an effort to improve
                the everyday lives of not only Drexel’s Greek life, but the
                university’s students as a whole. Partnering with Drexel’s
                student affairs office and Associate Dean John Cooke, SigEp is
                establishing a biweekly street cleaning project on Drexel’s 34th
                street.
              </p>
              <p>
                The street cleaning utilizes the manpower of our chapter to
                pick up and dispose of the trash that litters the sidewalks
                along the street. Currently, we cover the sidewalks on both
                sides of 34th street between Powelton Avenue and Lancaster
                Avenue. The idea for such a street cleaning event was first
                thought of by our own Chris Baccash in November of this past
                year. Chris has spearheaded the completely voluntary event ever
                since and is responsible for our partnership with Drexel’s
                student affairs office. So far, SigEp has received help from
                the sisters of Alpha Sigma Alpha, and we are currently
                encouraging any other fraternities and sororities to join this
                community service initiative.
              </p>
              <p>
                Although we are operating locally for now, we hope to soon
                extend the effort throughout Drexel’s campus community in the
                future. Street cleaning helps beautify our campus and city while
                improving the quality of life of all Drexel students. Getting
                rid of unnecessary trash alongside our thoroughfares on campus
                not only eliminates the obvious eyesore it creates, but also is
                a benefit to our urban environment. We have ambitious plans for
                our street cleaning event, and there is no community service
                effort on campus quite like it. As a casual observer of our
                campus environment, any bystander can see that an effort like
                this is needed. Drexel SigEp’s plans for this event do not have
                a timetable. As long as there is trash on our sidewalks, we plan
                to keep cleaning.
              </p>
